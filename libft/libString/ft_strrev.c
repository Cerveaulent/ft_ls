char	*ft_strrev(char *s)
{
	char	c;
	int		i;
	int		j;

	j = 0;
	while (s[j])
		j++;
	i = -1;
	while (++i < --j)
	{
		c = s[i];
		s[i] = s[j];
		s[j] = c;
	}
	return (s);
}
